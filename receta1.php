        <ol id="preparacion" class="preparacion">
           <li> Precalentar el horno a 190ºC, y engrasar un molde de 22cm de largo.</li>
            <li> Mezclar los ingredientes secos: harina, azúcar, especias y levadura en polvo en un bol.</li>
                <li> Batir los huevos y añadir la margarina/ mantequilla y <strong>la miel</strong>, mezclar bien y añadir al bol con los ingredientes secos. Batirlo todo hasta que quede una mezcla homogénea.</li>
